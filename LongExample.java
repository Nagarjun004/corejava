class LongExample{
	
	public static void main(String[] values){
		
		String numberOfOrganism="200000000000";
		long convertedNumberOfOrganism=Long.parseLong(numberOfOrganism);
		System.out.println(convertedNumberOfOrganism);
		
		String numberOfPopulation="30000000000";
		long convertedNumberOfPopulation=Long.parseLong(numberOfPopulation);
		System.out.println(convertedNumberOfPopulation);
		
		String numberOfStars="400000000000";
		long convertedNumberOfStars=Long.parseLong(numberOfStars);
		System.out.println(convertedNumberOfStars);
		
		String numberOfVehicles="700000000000";
		long convertedNumberOfVehicles=Long.parseLong(numberOfVehicles);
		System.out.println(convertedNumberOfVehicles);
		
		String numberOfSpecies="10000000000000";
		long convertedNumberOfSpecies=Long.parseLong(numberOfSpecies);
		System.out.println(convertedNumberOfSpecies);
		
	}
	
	
}